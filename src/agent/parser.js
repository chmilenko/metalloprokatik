/**
 * parser.js
 * 
 * Парсит свободный текст заявки от менеджера.
 * Отправляет в AI → получает структурированный JSON.
 * 
 * Вход:  "Труба ВГП ДУ15 стенка 2 — 3 тонны"
 * Выход: [{ название, поисковый_запрос, параметры, количество, единица }]
 */

const { askAI } = require('./llm')
const searchMap = require('../data/searchMap.json')
async function parseOrder(text) {
  const prompt = `
Ты помощник менеджера по металлопрокату.
Извлеки список позиций из заявки и верни ТОЛЬКО JSON массив.
Никакого текста до или после JSON. Никаких markdown блоков.

Заявка:
${text}

Формат каждой позиции:
{
  "название": "человекочитаемое название позиции",
  "поисковый_запрос": "одно короткое слово для поиска на сайте металлопроката",
  "параметры": {
    "ду": число или null,
    "диаметр": число или null,
    "стенка": число или null,
    "толщина": число или null,
    "ширина": число или null,
    "длина_листа": число или null,
    "марка": "строка или null"
  },
  "количество": число,
  "единица": "т или м или шт или кг"
}

Правила для поискового запроса:
- Труба ВГП → "ВГП"
- Труба электросварная → "электросварная"
- Арматура → "арматура"
- Лист горячекатаный, лист г/к → "лист горячекатаный"
- Уголок → "уголок"
- Швеллер → "швеллер"
- Балка → "балка"
- Круг → "круг"
- Полоса → "полоса"

Правила для параметров листа:
- "лист 2.5x1250x2500" → толщина=2.5, ширина=1250, длина_листа=2500
- "лист 10х1500х6000" → толщина=10, ширина=1500, длина_листа=6000
- Разделитель может быть x, х, *, x

Правила для труб:
- "ДУ15" или "ДУ 15" → ду=15
- "стенка 2" или "х2" → стенка=2
- "диаметр 12" или "д12" → диаметр=12

Пример входа:
"Труба ВГП ДУ15 стенка 2 три тонны
Арматура А500С диаметр 12 пять тонн
лист г/к 2.5x1250x2500 одна тонна"

Пример выхода:
[
  {
    "название": "Труба ВГП ДУ15 стенка 2",
    "поисковый_запрос": "ВГП",
    "параметры": { "ду": 15, "диаметр": null, "стенка": 2, "толщина": null, "ширина": null, "длина_листа": null, "марка": null },
    "количество": 3,
    "единица": "т"
  },
  {
    "название": "Арматура А500С диаметр 12",
    "поисковый_запрос": "арматура",
    "параметры": { "ду": null, "диаметр": 12, "стенка": null, "толщина": null, "ширина": null, "длина_листа": null, "марка": "А500С" },
    "количество": 5,
    "единица": "т"
  },
  {
    "название": "Лист г/к 2.5x1250x2500",
    "поисковый_запрос": "лист",
    "параметры": { "ду": null, "диаметр": null, "стенка": null, "толщина": 2.5, "ширина": 1250, "длина_листа": 2500, "марка": null },
    "количество": 1,
    "единица": "т"
  }
]
  Используй этот словарь для поискового запроса:
  ${JSON.stringify(searchMap.поисковые_запросы, null, 2)}
`

  const response = await askAI(prompt)

  try {
    const clean = response.replace(/```json|```/g, '').trim()
    const positions = JSON.parse(clean)
    console.log('=== ПАРСИНГ ===', JSON.stringify(positions, null, 2))
    return positions
  } catch (err) {
    throw new Error(`Не удалось распарсить ответ AI: ${response}`)
  }
}

module.exports = { parseOrder }