const { askAI } = require('./llm')
const { loadMap } = require('./searchMapManager')

async function parseOrder(text) {
  const searchMap = loadMap()

  const prompt = `
Ты помощник менеджера по металлопрокату.
Извлеки список позиций из заявки и верни ТОЛЬКО JSON массив.
Никакого текста до или после JSON. Никаких markdown блоков.

Заявка:
${text}

Используй этот словарь для поискового запроса — если название есть в словаре, используй соответствующий запрос:
${JSON.stringify(searchMap.поисковые_запросы, null, 2)}

Формат каждой позиции:
{
  "название": "человекочитаемое название позиции",
  "поисковый_запрос": "запрос для поиска на mc.ru",
  "параметры": {
    "ду": число или null,
    "диаметр": число или null,
    "стенка": число или null,
    "толщина": число или null,
    "ширина": число или null,
    "длина_листа": число или null,
    "марка": "строка или null"
  },
  "количество": число,
  "единица": "т или м или шт или кг"
}

ПРАВИЛА ДЛЯ ПОИСКОВОГО ЗАПРОСА:

1. Трубы круглые:
   - Труба ВГП, труба водогазопроводная → "ВГП"
   - Труба электросварная, э/с труба → "электросварная"
   - Труба бесшовная г/д, горячедеформированная → "трубы горячедеформированные [диаметр]x[стенка]"
   - Труба бесшовная х/д, холоднодеформированная → "трубы холоднодеформированные [диаметр]x[стенка]"
   - Труба нержавеющая круглая → "труба нержавеющая [диаметр]x[стенка]"
   - Труба чугунная → "труба чугунная"

2. Трубы профильные:
   - Если оба размера ОДИНАКОВЫЕ (квадратная) → "Трубы электросварные ЭСВ квадратные [сторона]х[стенка]"
     Пример: 40*40*3 → "Трубы электросварные ЭСВ квадратные 40х3"
   - Если размеры РАЗНЫЕ (прямоугольная) → "Трубы электросварные ЭСВ прямоугольные [А]х[Б]х[стенка]"
     Пример: 40*25*3 → "Трубы электросварные ЭСВ прямоугольные 40х25х3"

3. Сортовой прокат:
   - Уголок равнополочный 100х100х7 → "уголок 100х7"
   - Уголок неравнополочный 100х65х8 → "уголок 100х65х8"
   - Швеллер 16П → "швеллер 16 П" (ВАЖНО: пробел перед П или У!)
   - Швеллер 12П → "швеллер 12 П"
   - Швеллер низколегированный 16П → "швеллер низколегированный 16 П"
   - Балка 20 → "балка 20"
   - Круг 14 → "круг 14"
   - Полоса 40х4 → "полоса 40х4"
   - Арматура д12 → "арматура 12"
   - Арматура д16 А500С → "арматура 16"

4. Листовой прокат — ВАЖНО включать все параметры в запрос:
   - Лист горячекатаный, лист г/к → "лист горячекатаный"
   - Лист холоднокатаный, лист х/к → "лист холоднокатаный"
   
   - Лист нержавеющий — ВСЕГДА включай толщину, ширину и тип поверхности:
     "Лист нержавеющий х/к 0.4х1000 BA+PE зеркальный" → "нержавеющий лист х/к 0.4х1000 BA+PE (зеркальный)"
     "Лист нержавеющий х/к 0.4х1000 2B матовый" → "нержавеющий лист х/к 0.4х1000 2B (матовый)"
     "Лист нержавеющий х/к 0.6х1250 4N шлифованный" → "нержавеющий лист х/к 0.6х1250 4N+PE (шлифованный)"
     "Лист нержавеющий х/к 1.0х1000 зеркальный AISI 304" → "нержавеющий лист х/к 1.0х1000 зеркальный AISI 304"
   
   - Лист оцинкованный — включай все параметры:
     "Лист оцинкованный 0.9х1250х2500 2кл КР" → "Лист оцинкованный 0.9х1250х2500 2 кл КР пром_пас"
   
   - Лист мостостроительный, конструкционный — включай марку и размеры:
     "Лист горячекатаный мостостр 30х2500х12000" → "лист горячекатаный 30х2500х12000 мостостроительный"

5. Если позиция есть в словаре — используй запрос из словаря.

6. Если не знаешь — используй полное название позиции как написано в заявке.

ПРАВИЛА ДЛЯ ПАРАМЕТРОВ:

Листы:
- "лист 2.5x1250x2500" → толщина=2.5, ширина=1250, длина_листа=2500
- "лист 0.4х1000" → толщина=0.4, ширина=1000, длина_листа=null (рулон)

Трубы круглые:
- "ДУ15" или "ДУ 15" → ду=15
- "стенка 2" или "х2" или "x2" → стенка=2
- "диаметр 12" или "д12" → диаметр=12

ПРИМЕРЫ:

Вход: "Лист нержавеющий х/к 0.4х1000х2000 BA+PE (зеркальный) - 1т"
Выход: поисковый_запрос="нержавеющий лист х/к 0.4х1000 BA+PE (зеркальный)", толщина=0.4, ширина=1000, длина_листа=2000

Вход: "Лист нержавеющий х/к 0.4х1000 2B (матовый) - 1т"
Выход: поисковый_запрос="нержавеющий лист х/к 0.4х1000 2B (матовый)", толщина=0.4, ширина=1000

Вход: "Лист горячекатаный мостостр 30х2500х12000 - 1т"
Выход: поисковый_запрос="лист горячекатаный 30х2500х12000 мостостроительный", толщина=30, ширина=2500, длина_листа=12000

Вход: "лист 1.5х1000х2000 хк - 220шт"
Выход: поисковый_запрос="лист холоднокатаный", толщина=1.5, ширина=1000, длина_листа=2000, количество=220, единица="шт"

Вход: "40*25*3=60 м"
Выход: поисковый_запрос="Трубы электросварные ЭСВ прямоугольные 40х25х3", количество=60, единица="м"

Вход: "60*60*3=60 м"
Выход: поисковый_запрос="Трубы электросварные ЭСВ квадратные 60х3", количество=60, единица="м"

Вход: "Швеллер 16П 18м"
Выход: поисковый_запрос="швеллер 16 П", количество=18, единица="м"

Вход: "Труба ВГП ДУ15 стенка 2 одна тонна"
Выход: поисковый_запрос="ВГП", параметры.ду=15, параметры.стенка=2, количество=1, единица="т"
`

  const response = await askAI(prompt)

  try {
    const clean = response.replace(/```json|```/g, '').trim()
    const positions = JSON.parse(clean)
    console.log('=== ПАРСИНГ ===', JSON.stringify(positions, null, 2))
    return positions
  } catch (err) {
    throw new Error(`Не удалось распарсить ответ AI: ${response}`)
  }
}

module.exports = { parseOrder }