/**
 * parser.js
 * 
 * Парсит свободный текст заявки от менеджера.
 * Отправляет в Claude → получает структурированный JSON.
 * 
 * Вход:  "Труба ВГП ДУ15 стенка 2 — 3 тонны"
 * Выход: [{ тип, параметры, количество, единица }]
 */

const { askAI } = require('./llm')

async function parseOrder(text) {
  const prompt = `
Ты помощник менеджера по металлопрокату.
Извлеки список позиций из заявки и верни ТОЛЬКО JSON массив.
Никакого текста до или после JSON.

Заявка:
${text}

Формат каждой позиции:
{
  "название": "человекочитаемое название",
  "поисковый_запрос": "короткий запрос для поиска на сайте (например: ВГП, арматура, лист)",
  "параметры": {
    "ду": число или null,
    "диаметр": число или null,
    "стенка": число или null,
    "толщина": число или null,
    "марка": "строка или null"
  },
  "количество": число,
  "единица": "т или м или шт или кг"
}

Пример входа: "Труба ВГП ДУ15 стенка 2 три тонны, арматура А500С д12 пять тонн"
Пример выхода:
[
  {
    "название": "Труба ВГП ДУ15 стенка 2",
    "поисковый_запрос": "ВГП",
    "параметры": { "ду": 15, "диаметр": null, "стенка": 2, "толщина": null, "марка": null },
    "количество": 3,
    "единица": "т"
  },
  {
    "название": "Арматура А500С д12",
    "поисковый_запрос": "арматура",
    "параметры": { "ду": null, "диаметр": 12, "стенка": null, "толщина": null, "марка": "А500С" },
    "количество": 5,
    "единица": "т"
  }
]
`

  const response = await askAI(prompt)

  try {
    // Убираем возможные markdown блоки если Claude их добавил
    const clean = response.replace(/```json|```/g, '').trim()
    return JSON.parse(clean)
  } catch (err) {
    throw new Error(`Не удалось распарсить ответ Claude: ${response}`)
  }
}

module.exports = { parseOrder }